    private void button2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button2ActionPerformed
        pro.myDrive();

        int st = Integer.parseInt(sto.getText());
        int q = Integer.parseInt(Qua.getText());
        int sup = st - q;
        sqls = "Insert into invoice values(" + Integer.parseInt(billnum.getText())
                + ",'" + d.getText() + "','" + pname.getSelectedItem() + "'," + Integer.parseInt(UniteP.getText())
                + "," + q + "," + Integer.parseInt(tot.getText()) + ")";
        try {
            stm = pro.con.createStatement();
            stm.executeUpdate(sqls);
        } catch (SQLException sqle) {
            JOptionPane.showMessageDialog(null, sqle);
        }

        int pr = Integer.parseInt(pi.getText());
        try {
            String strg = "update product set stock=? where product_id=?";
            pprds = pro.con.prepareStatement(strg);
            pprds.setInt(1, sup);
            pprds.setInt(2, pr);
            pprds.executeUpdate();

        } catch (SQLException sqle) {
            JOptionPane.showMessageDialog(null, sqle);
        }


    }//GEN-LAST:event_button2ActionPerformed

