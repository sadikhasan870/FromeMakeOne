    private void QuaFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_QuaFocusLost
        stoW = Integer.parseInt(sto.getText());
        quaW = Integer.parseInt(Qua.getText());
        upW = Double.parseDouble(UniteP.getText());

        if (stoW < quaW) {
            JOptionPane.showMessageDialog(sto, "Stock is not available");
            Qua.requestFocus();
        } else {
            stoW = stoW - quaW;
            sto.setText(String.valueOf(stoW));
            totW = quaW * upW;
            tot.setText(String.valueOf(totW));
        }


    }//GEN-LAST:event_QuaFocusLost

